// Initialize the game: initialize all sprites and 
// reset refresh control and game status.
void Game_Init(void){
	Sound_Init();
  time_to_draw=false;
	game_s = true;
	end_prompt = false;
	flaghit = 0;
	tempScore = 0;
	
  // Version 1: add enemy initialization with close posture.
	 for(int i=0;i<(current_level + 1);i++){
		 if(i <= 3){
			Enemy[i].x = 15*i;
			Enemy[i].y = 10;
			if(i == 3 || i > 3 && i <= 5){
				Enemy[i].image = SmallEnemyPointA[i - 3];
			}
		}
		else if(i > 3 && i <= 5){
			Enemy[i].x = 15*(i - 4) - 10;
			Enemy[i].y = 20;
		}
		else{
			Enemy[i].x = 15*(i - 4) - 10;
			Enemy[i].y = 20;
			Enemy[i].image = SmallEnemyPointA[i - 6];
		}
		Enemy[i].life = true;
		Enemy[i].collision = false;
		Enemy[i].enemy_posture = false;
  }

  // Version 2: add player ship initialization
	PlayerShip.x = ADCValue_To_X_AXIS(ADC1SS3_In(),MAXWIDTH);
	PlayerShip.y = 47;
	PlayerShip.image = PlayerShip0;
 
  // Version 3: Add bullet initialization: you can choose Laser or Missile
	Bullet.y = 47 - 9;
	Bullet.image = Laser0;
	Bullet.life = false;//bec no bullet has been shot yet in init
}

// Update positions for all alive sprites.
void Move(void){
  uint8_t num_life = (current_level + 1);
	float SPEED_ALIEN[SIZE] = {1.0,1.2,1.4,1.6,1.7,1.8,1.9,2.2};//each time current level goes up we iterate through this
  // V1: Move enemies: modify x, check life:alive or dead: dead if right side reaches right screen border or detect a hit
  // Change postures for the enemies: two postures: open/close arms/legs
	// If current position is close, then next position will be open; vise versa.
		for(int i=0;i<(current_level + 1);i++){//each new level increase the alien move speed
			if(Enemy[i].x < MAX_WIDTH_ALIEN && Enemy[i].life == true){
				Enemy[i].x += SPEED_ALIEN[current_level];//update posistion by one pixel
			}
			else{
				Enemy[i].life = false;
				num_life -= 1;
			}

			if(Enemy[i].enemy_posture == false && i <= 3 || Enemy[i].enemy_posture == false && i > 5 || 
				Enemy[i].enemy_posture == false && i > 3 && i <= 5){
				Enemy[i].enemy_posture = true;//make open
				if(i == 3 || i > 3 && i <= 5){//if we are on 3 enemy make it print from beginning of 2d array
					Enemy[i].image = SmallEnemyPointB[i - 3];//print from beginngin 3 -3 = 0
				}
				else if(i > 5){
					Enemy[i].image = SmallEnemyPointB[i - 6];//6 - 6 = 0 and 7 - 6 = 1
				}
				else{
					Enemy[i].image = SmallEnemyPointB[i];//if not 3 then just normal
				}
			}
			else{
				Enemy[i].enemy_posture = false;
				if(i == 3 || i > 3 && i <= 5){//if we are on 3 enemy make it print from beginning of 2d array
					Enemy[i].image = SmallEnemyPointA[i - 3];//print from beginngin 3 -3 = 0
				}
				else if(i > 5){
					Enemy[i].image = SmallEnemyPointA[i - 6];//6 - 6 = 0 and 7 - 6 = 1
				}
				else{
					Enemy[i].image = SmallEnemyPointA[i];//if not 3 then just normal
				}
			}
		}
		
	// V2: Read ADC and update player ship position: only x coordinate will be changed. 
		PlayerShip.x = ADCValue_To_X_AXIS(ADC1SS3_In(),MAXWIDTH);
		//check if bullet hits the top
		if(Bullet.y > 0 + LASERH && Bullet.life == true && current_level <= 1){
			Bullet.y -= 1.5;
		}
		else if(Bullet.y > 0 + LASERH && Bullet.life == true && 1 < current_level && current_level <= 3){
			Bullet.y -= 3;
		}
		else if(Bullet.y > 0 + LASERH && Bullet.life == true &&  current_level > 3){
			Bullet.y -= 5;
		}
		else{
			Bullet.life = false;
		}
		
		//check for collision
		for(int i =0; i<(current_level + 1);i++){
			if(Enemy[i].collision == true){
				num_life -= 1;
				Enemy[i].life = false;
				Enemy[i].collision = false;
				Enemy[i].x = 83;
			}
		}
		//check for hit from bullet make bullet dead
		for(int i =0;i<(current_level + 1);i++){
			if(Enemy[i].x < (Bullet.x + BULLETW) && Bullet.x < (Enemy[i].x + ALIEN_WIDTH) && Enemy[i].y < (Bullet.y + BULLETH) &&
				Bullet.y < (Enemy[i].y + ALIEN_HEIGHT) && Bullet.life == true){
					Bullet.life = false;
					Enemy[i].collision = true;
					Enemy[i].image = SmallExplosion0;
					Sound_Explosion();
					Delay100ms(1);//play the explosion for 0.1sec
					tempScore += 1;
					flaghit += 1;//everytime a hit is detected we increment this flag 
			}
		}
  if (flaghit==(current_level + 1) && last_level == false && num_life == 0){//if we detect a hit and person clears all sprites of that level move to next
		current_level = current_level + 1;//increment to next level
		if(current_level == 7){
			last_level = true;
		}
		total_score += tempScore;//update total score from the score of that level
		Level_Prompt();//display level prompt
		Game_Init();//reInit all sprites
  }
	//if num_life is equal to zero meaning all aliens have died either to bullet or end of screen
	// and the flaghit does not equal to the amount of aliens killed for that level game over
	else if(num_life == 0 && flaghit !=(current_level + 1) ||	last_level == true && num_life == 0){
		 game_s = OVER;
		 current_level = 0;//restart all the levels
		 total_score += tempScore;//update the score from the last level if they didnt kill all sprites
		 last_level = false;
	}
}


// Update the screen: 
// clear display and update the screen with the 
// current positions of all sprites that are alive.
void Draw(void){
  uint8_t i;
  
  if (game_s==false) return;
  
  Nokia5110_ClearBuffer();
  
  // V1:Update live enemies' positions in display buffer: screen[]
  for(i=0;i<(current_level + 1);i++){
    if(Enemy[i].life == true){//if life is equal to true which is alive in the ENUM
			 Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image, 0);  // update screen[]
    }
  }
  
  // V2: Update the playership position in display buffer: screen[]
		Nokia5110_PrintBMP(PlayerShip.x,PlayerShip.y,PlayerShip.image,0);
	
  // V3: Update the bullet position in display buffer if there is one.
  if (Bullet.life==true) {
		Nokia5110_PrintBMP(Bullet.x,Bullet.y,Bullet.image,0);
  }

  Nokia5110_DisplayBuffer();      // Update the display with information in display buffer screen[].
	}
// Control screen refresh rate. 
void SysTick_Handler(void){
	// signal time to refresh
	if(game_s == true && end_prompt == false){//if the game is ongoing refresh the screen every 0.2sec
		time_to_draw = true;//make true after completing 0.2s counting
	}
	else{
		if(count3sec != 15){
			count3sec = count3sec + 1;
		}
	}
}

void GPIOPortF_Handler(void){    // called on release of either SW1 or SW2
	// take care of button debounce
	for(int i = 0; i < 160000; i++){};
	// SW1: shoot a bullet if there is none.
		if(GPIO_PORTF_RIS_R&SW1_MASK && Bullet.life == false){
				Bullet.life = true;
				Bullet.x = PlayerShip.x + 8;
				Bullet.y = 47 - 9;
				Sound_Shoot();
				GPIO_PORTF_ICR_R = SW1_MASK;  //ack flag for SW1
			}else{
				GPIO_PORTF_ICR_R = SW1_MASK;  //ack flag for SW1
			}
	// SW2: start the game, change the game status to ON
		if(GPIO_PORTF_RIS_R&SW2_MASK && end_prompt == false){//check if end_prompt is playing
			game_s = true;//change to true or ON when the button is pressed
			GPIO_PORTF_ICR_R = SW2_MASK;  //ack flag for SW2
		}
		else{
			GPIO_PORTF_ICR_R = SW2_MASK;  //ack flag for SW2
		}
	}
void threeSecDelay(void){
	while(count3sec != 15){};
		SysTickStop();//stop the timer so interrupt doesnt keep incrementing
		count3sec = 0;//clear the counter
}
void Delay100ms(uint32_t count){
  uint32_t time;
  while(count > 0){
    time = 333333;  // 0.1 sec at 80 MHz
    while(time){
      time--;
    }
    count--;
  }
}

