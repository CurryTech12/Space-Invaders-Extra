void System_Init(void){
  DisableInterrupts();
  PLL_Init();   	// set system clock to 80 MHz
	SysTick_Init(REFRESH_RATE_LOWEST);
  Switch_Init();
  ADC1SS3_Init();
  Nokia5110_Init();
  Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer();      // draw buffer
  EnableInterrupts();
}

